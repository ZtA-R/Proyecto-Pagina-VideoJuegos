	ADD CONSTRAINT fk_videojoc_genere FOREIGN KEY (id_videojoc) REFERENCES videojoc(id),
    ADD CONSTRAINT fk_genere_videojoc FOREIGN KEY (id_genere) REFERENCES genere(id);
