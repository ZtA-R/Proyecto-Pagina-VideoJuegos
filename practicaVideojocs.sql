CREATE TABLE videojoc(
id INT AUTO_INCREMENT,
titol VARCHAR(50) NOT NULL,
preu DECIMAL(10,2),
data_sortida DATE,
PRIMARY KEY (id)
);

CREATE TABLE creador(
id INT AUTO_INCREMENT,
nom VARCHAR(50) NOT NULL,
cognom VARCHAR(50),
data_naix DATE,
PRIMARY KEY (id)
);

CREATE TABLE videojoc_creador(
id INT AUTO_INCREMENT,
id_videojoc INT NOT NULL,
id_creador INT NOT NULL,
PRIMARY KEY (id)
);

CREATE TABLE genere(
id INT AUTO_INCREMENT,
nom VARCHAR(50) NOT NULL,
descripcio VARCHAR(200),
PRIMARY KEY (id)
);

CREATE TABLE videojoc_genere(
id INT AUTO_INCREMENT,
id_videojoc INT NOT NULL,
id_genere INT NOT NULL,
PRIMARY KEY (id)
);

CREATE TABLE valoracio(
id INT AUTO_INCREMENT,
puntuacio INT NOT NULL,
comentari VARCHAR(200),
id_videojoc INT NOT NULL,
id_usuari INT NOT NULL,
data_publicacio DATE,
PRIMARY KEY (id)
);

CREATE TABLE usuari(
id INT AUTO_INCREMENT,
nom VARCHAR(50) NOT NULL,
cognom VARCHAR(50),
email VARCHAR(100) NOT NULL,
PRIMARY KEY (id)
);

